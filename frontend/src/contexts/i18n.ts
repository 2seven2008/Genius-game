export type TKey = keyof (typeof translations)["pt-BR"];

export function useT() {
  const lang = useThemeStore((s) => s.language);
  return (key: TKey): string => {
    const t = translations[lang] as Record<string, string>;
    return (
      t[key] ?? (translations["pt-BR"] as Record<string, string>)[key] ?? key
    );
  };
}
